#ifndef remove_reference_h
#define remove_reference_h

template <typename T>
struct remove_reference {
    using type = T;
};

template <typename T>
struct remove_reference<T&> {
    using type = T;
};

template <typename T>
struct remove_reference<T&&> {
    using type = T;
};

template <typename T>
using remove_reference_t = typename  remove_reference<T>::type;

#endif /* remove_reference_h */
